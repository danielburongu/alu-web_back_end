#!/usr/bin/env python3
def add(a: float, b: float) -> float:
    """
    Takes two float numbers a and b and returns their sum as a float.
    
    Parameters:
    a (float): The first number to be added.
    b (float): The second number to be added.

    Returns:
    float: The sum of the two numbers.
    """
    return a + b
